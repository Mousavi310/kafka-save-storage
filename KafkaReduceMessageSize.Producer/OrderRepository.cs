                .Select(d => new OrderAvroModel
                    {
                        CreationTime = d.CreationTime,
                        CustomerId  = d.CustomerId,
                        Id = d.Id,
                        ProductId = d.ProductId,
                        Status = d.Status
                    })
                .ToList();  
